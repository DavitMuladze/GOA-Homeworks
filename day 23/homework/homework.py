# Lists (სია) არის მონაცემთა სტრუქტურა, რომელიც ინახავს ელემენტებს
# ისინი შეიძლება შეიცავდეს სხვადასხვა ტიპის მონაცემებს, მაგალითად, რიცხვებს, სტრინგებს, ან თუნდაც სხვა სიებს.

my_list = ["apple", "banana", "cherry"]  # ეს არის სია

# Indexing (ინდექსირება) საშუალებას გვაძლევს მივწვდეთ კონკრეტულ ელემენტს სიის შიგნით
print(my_list[0])  # გამოიტანს "apple" (პირველი ელემენტი, ინდექსი 0)
print(my_list[1])  # გამოიტანს "banana" (მეორე ელემენტი, ინდექსი 1)
print(my_list[-1])  # გამოიტანს "cherry" (ბოლო ელემენტი, უარყოფითი ინდექსი -1)
